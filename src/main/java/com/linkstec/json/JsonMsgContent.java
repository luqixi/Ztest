package com.linkstec.json;

public class JsonMsgContent {

	private String first;
	private String fundAccount;
	private String exchangeTime;
	private String exchangeType;
	private String exchangeCounts;
	private String exchangeFree;
	private String remark;
	private Double doubl;
	public String getFirst() {
		return first;
	}
	public void setFirst(String first) {
		this.first = first;
	}
	public String getFundAccount() {
		return fundAccount;
	}
	public void setFundAccount(String fundAccount) {
		this.fundAccount = fundAccount;
	}
	public String getExchangeTime() {
		return exchangeTime;
	}
	public void setExchangeTime(String exchangeTime) {
		this.exchangeTime = exchangeTime;
	}
	public String getExchangeType() {
		return exchangeType;
	}
	public void setExchangeType(String exchangeType) {
		this.exchangeType = exchangeType;
	}
	public String getExchangeCounts() {
		return exchangeCounts;
	}
	public void setExchangeCounts(String exchangeCounts) {
		this.exchangeCounts = exchangeCounts;
	}
	public String getExchangeFree() {
		return exchangeFree;
	}
	public void setExchangeFree(String exchangeFree) {
		this.exchangeFree = exchangeFree;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public Double getDoubl() {
		return doubl;
	}
	public void setDdoubl(Double doubl) {
		this.doubl = doubl;
	}
	
}
